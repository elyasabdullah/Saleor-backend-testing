import * as Types from '../../../types';

import { gql } from '@apollo/client';
import * as Apollo from '@apollo/client';
const defaultOptions = {} as const;
export type PasswordResetMutationVariables = Types.Exact<{
  input: Types.PasswordResetInput;
}>;


export type PasswordResetMutation = { passwordReset?: { success: boolean, errors: Array<{ field?: string | null, message: string, code: string } | null> } | null };


export const PasswordResetDocument = gql`
    mutation PasswordReset($input: PasswordResetInput!) {
  passwordReset(input: $input) {
    success
    errors {
      field
      message
      code
    }
  }
}
    `;
